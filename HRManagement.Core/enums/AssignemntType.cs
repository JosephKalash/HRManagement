namespace HRManagement.Core.enums
{
    public enum AssignmentType
    {
        Permanent,           // Permanent role or assignment
        Temporary,           // Temporary assignment for a limited period
        Acting,              // Acting role (e.g., filling in for a superior)
        Delegated,           // Authority or responsibility delegated from another
        Secondment,          // Temporarily assigned to another department/unit
        Training,            // Assignment for training or professional development
        SpecialDuty,         // Special or exceptional assignment
        Contractual          // Assignment based on a service contract
    }
}